///
/// @brief 一手詰のテストを行い、失敗した場合は詳細なメッセージを出力します。
///
static ::testing::AssertionResult AssertMate1(const char* pos_expr,
                                              const char* answer_expr,
                                              Position& pos,
                                              const std::vector<std::string>& answer_list) {

    // Mate1plyは指し手とその評価値(詰んでいたらVALUE_MATE)を返します。
    Move move = pos.side_to_move() == BLACK ?
        pos.Mate1ply<BLACK>() :
        pos.Mate1ply<WHITE>();

    std::string move_sfen = move_to_uci(move);
    auto it = std::find(answer_list.begin(), answer_list.end(), move_sfen);

    // 指し手と評価値を両方確認
    if (move != MOVE_NONE && it != answer_list.end()) {
        return ::testing::AssertionSuccess();
    }

    Move correctMove = move_from_uci(pos, answer_list[0]);
    return ::testing::AssertionFailure()
        << "Mate1 test is failure." << std::endl
        << "sfen: " << pos.to_fen() << std::endl
        << position_to_kif(pos) << std::endl
        << "Correct: " << move_to_uci(correctMove) << " " << move_to_kif(correctMove) << std::endl
        << "Actual:  " << move_to_uci(move) << " " <<  move_to_kif(move);
}

///
/// @brief 一手詰を詰ますことができるかテストします。
///
TEST (MateTest, mate1_test)
{
    for (auto mateTest : mate1_table)
    {
        Position pos(mateTest.position_sfen, 0);

        EXPECT_PRED_FORMAT2(AssertMate1, pos, mateTest.answer_list);
    }
}

